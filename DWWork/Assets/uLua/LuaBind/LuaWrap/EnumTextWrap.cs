	[MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
	static int IntToEnum(IntPtr L)
	{
		int arg0 = (int)LuaDLL.lua_tonumber(L, 1);
		EnumText o = (EnumText)arg0;
		LuaScriptMgr.PushEnum(L, o);
		return 1;
	}
